using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Wodsoft.UI.Media
{
    /// <summary>
    ///     AlignmentY - The AlignmentY enum is used to describe how content is positioned 
    ///     vertically within a container.
    /// </summary>
    public enum AlignmentY
    {
        /// <summary>
        ///     Top - Align contents towards the top of a space.
        /// </summary>
        Top = 0,

        /// <summary>
        ///     Center - Center contents vertically.
        /// </summary>
        Center = 1,

        /// <summary>
        ///     Bottom - Align contents towards the bottom of a space.
        /// </summary>
        Bottom = 2,
    }
}
